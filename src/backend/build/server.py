def find_gcd(a: int = None, b: int = None) -> int:
    """Calculate the greatest common divisor (GCD) of two integers using Euclidean algorithm"""
    # Handle negative numbers by taking absolute values
    a = abs(a)
    b = abs(b)
    
    # Euclidean algorithm
    while b != 0:
        temp = b
        b = a % b
        a = temp
    
    return a

